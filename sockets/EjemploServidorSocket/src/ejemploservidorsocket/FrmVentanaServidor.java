    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if (!txtMensaje.getText().equals("")) {
            String mensaje = "Servidor: " + txtMensaje.getText();
            txtHistorial.append(mensaje + "\n");
            try {
                canalSalida.writeUTF(mensaje);
            } catch (IOException ex) {
                Logger.getLogger(FrmVentanaServidor.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtMensaje.setText("");
        }
        txtMensaje.requestFocus();
    }//GEN-LAST:event_btnEnviarActionPerformed

    
