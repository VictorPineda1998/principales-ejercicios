    public FrmVentanaServidor() {
        initComponents();
        txtMensaje.requestFocus();
        try {
            servicio = new ServerSocket(4000);
            System.out.println("Esperando a que un cliente se conecte...");
            servidor = servicio.accept();
            System.out.println("Se ha conectado el cliente.");

            canalEntrada = new DataInputStream(servidor.getInputStream());
            oido = new Oido(canalEntrada, txtHistorial);
            oido.start();

            canalSalida = new DataOutputStream(servidor.getOutputStream());
        } catch (IOException error) {
            System.out.println("Error de conexion");
        }
    }
    
